MODE_MAPS = {
    TinyTextMode.SMALL_CAPS: SMALL_CAPS_MAP,
    TinyTextMode.SUPERSCRIPT: SUPERSCRIPT_MAP,
    TinyTextMode.SUBSCRIPT: SUBSCRIPT_MAP,
}


def transform_text(text: str, mode: TinyTextMode) -> str:
    character_map = MODE_MAPS[mode]
    transformed: list[str] = []

    for char in text:
        if char.lower() in character_map:
            transformed.append(character_map.get(char, char))
        else:
            transformed.append(char)

    return "".join(transformed)


def all_variants(text: str) -> dict[str, str]:
    return {mode.value: transform_text(text, mode) for mode in TinyTextMode}
